def get_co2_data():
    """
    Return annual CO2 data.

    Returns
    -------
    years : numpy.ndarray
        Years from 1959 to 2021.
    co2 : numpy.ndarray
        CO2 concentration in ppm.
    """
    return co2_years.copy(), co2_ppm.copy()


def get_temperature_data():
    """
    Return annual temperature anomaly data.

    Returns
    -------
    years : numpy.ndarray
        Years from 1958 to 2021.
    temp : numpy.ndarray
        Global land-ocean temperature anomaly in degrees Celsius.
    """
    return temp_years.copy(), temp_anomaly.copy()


def get_aligned_co2_temperature_data():
    """
    Return CO2 and temperature data aligned by year.

    Since annual CO2 data starts in 1959 and temperature data starts in 1958,
    the aligned period is 1959-2021.

    Returns
    -------
    years : numpy.ndarray
        Common years.
    co2 : numpy.ndarray
        Annual CO2 concentration in ppm.
    temp : numpy.ndarray
        Temperature anomaly in degrees Celsius.
    """
    common_years = np.intersect1d(co2_years, temp_years)

    co2_indices = np.isin(co2_years, common_years)
    temp_indices = np.isin(temp_years, common_years)

    return (
        common_years.copy(),
        co2_ppm[co2_indices].copy(),
        temp_anomaly[temp_indices].copy()
    )


def get_monthly_co2_data(as_dataframe=False):
    """
    Return monthly CO2 data from the uploaded NOAA CSV.

    Parameters
    ----------
    as_dataframe : bool
        If False, return a dictionary of numpy arrays.
        If True, return a pandas DataFrame. This requires pandas.

    Returns
    -------
    dict or pandas.DataFrame
        Monthly CO2 data.
    """
    data = {
        "year": monthly_co2_year.copy(),
        "month": monthly_co2_month.copy(),
        "decimal_date": monthly_co2_decimal_date.copy(),
        "average": monthly_co2_average.copy(),
        "deseasonalized": monthly_co2_deseasonalized.copy(),
        "ndays": monthly_co2_ndays.copy(),
        "sdev": monthly_co2_sdev.copy(),
        "unc": monthly_co2_unc.copy(),
    }

    if as_dataframe:
        import pandas as pd
        return pd.DataFrame(data)

    return data


def get_monthly_co2_series(use_deseasonalized=False):
    """
    Return a simple monthly time series for modeling.

    Parameters
    ----------
    use_deseasonalized : bool
        If False, use the original monthly average CO2 values.
        If True, use deseasonalized CO2 values.

    Returns
    -------
    decimal_date : numpy.ndarray
        Decimal year values.
    co2 : numpy.ndarray
        Monthly CO2 values in ppm.
    """
    if use_deseasonalized:
        return monthly_co2_decimal_date.copy(), monthly_co2_deseasonalized.copy()

    return monthly_co2_decimal_date.copy(), monthly_co2_average.copy()


